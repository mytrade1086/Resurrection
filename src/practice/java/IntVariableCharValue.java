package practice.java;

class IntVariableCharValue
{
   public static void main(String args[])
   {
          int a='x';   // We have assigned a character value to a variable which is declared as int
          System.out.println(a);
   }
 }


//Output of this program:
//120
//This means that Java will automatically convert the char value to Unicode value and assign it to the variable which is declared as int